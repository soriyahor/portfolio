        <h1 class="titreVeille">Vanta.js</h1>

        <p>Vanta.js est une bibliothèque javascript qui permet d'ajouter facilement une animation interactive ou non dans l'arrière-plan d'un site web.</p>
        <p>Il est accessible par ce lien : <a href="https://www.vantajs.com/">https://www.vantajs.com/</a></p><br>
        <p>Cette bibliothèque permet :</p>
        <ul>
            <li>D'ajouter sur n'importe quel site web des animations en seulement quelques lignes de code</li>
            <li>D'ajouter un effet animé dans n'importe quel élément HTML</li>
            <li>De personnaliser selon le thème choisi (taille, couleur, vitesse…)</li>
            <li>De fonctionner sur vanilla JS, React, Angular, Vue…</li>
            <li>D'être utilisé avec la balise canvas du HTML sans télécharger un plugin (une extension pour ajouter des fonctionnalités) </li>
            <li>D'avoir une bonne résolution sur l'animation</li>
        </ul>
        <br>

